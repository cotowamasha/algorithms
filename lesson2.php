//- Дублирование одномерного массива через foreach - O(n)
//- Рекурсивная функция нахождения факториала числа - O(n^2)
function factorial($n)
{
    if ($n === 1) { //O(1)
        return $n; //O(1)
    }
    return $n * factorial($n - 1); // O(n*(n-1)) -> O(n^2)
}
