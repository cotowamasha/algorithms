function task3($a, $b){
    $arr = [];
    for ($i = 0; $i < $b; $i++){
        $arr[$i] = [];
        for ($j = 0; $j < $a; $j++){
            if($i === 0){
                $arr[$i][$j] = $j+1;
            }
            if($i > 0 && $j+1 === $a){
                $arr[$i][$j] = $arr[$i-1][$j] + 1;
            }
            if($i+1 === $b && $j+1 !== $a ){
                $arr[$b-1][$j] = 2*$a + $b - $j - 2;
            }
        }
    }
    return $arr;
}
